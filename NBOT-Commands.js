function getBlock(text) {
    var out = [];
    for (var i = 0; i < 8; i++) {
        var toPush = '';
        text.split(/(.)/).forEach(function (e) {
            if (!e) return;
            toPush += '▒▒' + bigWords[e.toLowerCase()][i] + '▒▒';
        });
        out.push(toPush);
    }
    return out.join('\n');
}

